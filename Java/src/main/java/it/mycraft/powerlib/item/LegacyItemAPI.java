    @Nullable
    public static Material getMaterial(String id) {
        return getMaterials(id);
    }

    @Nullable
    public static Material getMaterial(int id) {
        return getMaterials(String.valueOf(id));
    }

    @Nullable
    public static Material getMaterial(String id, String data) {
        return getMaterial(id + ":" + data);
    }


    @Nullable
    public static Material getMaterial(int id, int data) {
        return getMaterial(id + ":" + data);
    }

    private static Material getMaterials(String id){
        if (id.contains(":")) {
            for (LegacyItemAPI oldID : LegacyItemAPI.values()) {
                if (oldID.getTotalID().equals(id))
                    return oldID.getMaterial();
            }
        }else {
            for (LegacyItemAPI oldID : LegacyItemAPI.values()) {
                if (oldID.getID().equals(id))
                    return oldID.getMaterial();
            }
        }
        return null;
    }
